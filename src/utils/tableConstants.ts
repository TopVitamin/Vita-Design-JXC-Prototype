/**
 * 表格最小宽度常量。
 *
 * 之前各列表/查询/详情页把 minWidth 硬编码在 JSX 的
 * `Math.max(totalWidth, XXXX)` 里，值分散且语义不清。这里按表格类型
 * 收口为命名常量，方便统一调整与查阅。
 *
 * - 标准：基础资料 / 通用 CRUD / 查询统计 / 往来查询等，列较少
 * - 单据：销售/采购/退货等业务单据，含选择列 + 多业务字段 + 操作列
 * - 单据宽：单据详情/编辑页的明细表，列更多
 * - 紧凑：操作日志等子表
 */

export const TABLE_MIN_WIDTH = {
  /** 通用列表 / 查询页（约 6-9 列）：基础资料、往来查询、收付款登记等 */
  standard: 1100,
  /** 业务单据列表页 - 采购订单 / 采购退货 / 采购退货出库 */
  document: 1500,
  /** 业务单据列表页 - 销售订单 */
  documentSales: 1520,
  /** 业务单据列表页 - 采购入库 */
  documentReceipt: 1540,
  /** 业务单据列表页 - 销售出库 / 销售退货 / 销售退货入库（列最多） */
  documentWide: 1560,
  /** 紧凑子表（操作日志等） */
  compact: 720,
  /** Priority 查询列表（销售查询，略宽于 standard） */
  priority: 1080,
} as const;